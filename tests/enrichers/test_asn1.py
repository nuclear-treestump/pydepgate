class TestReadTLV(unittest.TestCase):
    """Coverage of read_tlv: short/long-form length, overrun, recovery."""

    def test_short_form_length_basic(self):
        # SEQUENCE { } empty: 30 00
        anomalies: list[DERAnomaly] = []
        tag, length, body, end = read_tlv(b"\x30\x00", 0, anomalies, "test")
        self.assertEqual(tag, TAG_SEQUENCE)
        self.assertEqual(length, 0)
        self.assertEqual(body, 2)
        self.assertEqual(end, 2)
        self.assertEqual(anomalies, [])

    def test_short_form_length_with_content(self):
        # INTEGER 0x42: 02 01 42
        anomalies: list[DERAnomaly] = []
        tag, length, body, end = read_tlv(
            b"\x02\x01\x42", 0, anomalies, "test",
        )
        self.assertEqual(tag, TAG_INTEGER)
        self.assertEqual(length, 1)
        self.assertEqual(body, 2)
        self.assertEqual(end, 3)
        self.assertEqual(anomalies, [])

    def test_long_form_length_one_octet(self):
        # SEQUENCE of length 200: 30 81 c8 followed by 200 bytes
        body_bytes = b"\x00" * 200
        buf = b"\x30\x81\xc8" + body_bytes
        anomalies: list[DERAnomaly] = []
        tag, length, body, end = read_tlv(buf, 0, anomalies, "test")
        self.assertEqual(tag, TAG_SEQUENCE)
        self.assertEqual(length, 200)
        self.assertEqual(body, 3)
        self.assertEqual(end, 203)
        self.assertEqual(anomalies, [])

    def test_long_form_length_two_octets(self):
        # SEQUENCE of length 1000: 30 82 03 e8 followed by 1000 bytes
        body_bytes = b"\x00" * 1000
        buf = b"\x30\x82\x03\xe8" + body_bytes
        anomalies: list[DERAnomaly] = []
        tag, length, body, end = read_tlv(buf, 0, anomalies, "test")
        self.assertEqual(length, 1000)
        self.assertEqual(end, 1004)
        self.assertEqual(anomalies, [])

    def test_length_overrun_clamps_to_buffer(self):
        # Tag says 100 bytes follow; only 5 actually present.
        buf = b"\x30\x64hello"  # 0x64 = 100
        anomalies: list[DERAnomaly] = []
        _, _, _, end = read_tlv(buf, 0, anomalies, "test")
        self.assertEqual(end, len(buf))
        self.assertTrue(any(a.kind == "length_overrun" for a in anomalies))

    def test_indefinite_length_recovers(self):
        # 0x80 = indefinite-length form (BER, not DER).
        buf = b"\x30\x80\x02\x01\x01\x00\x00"
        anomalies: list[DERAnomaly] = []
        _, _, _, end = read_tlv(buf, 0, anomalies, "test")
        self.assertEqual(end, len(buf))
        self.assertTrue(
            any(a.kind == "indefinite_length" for a in anomalies)
        )

    def test_non_minimal_length_recorded(self):
        # Length 5 encoded as long-form (81 05). Should record an
        # anomaly but still parse correctly.
        buf = b"\x30\x81\x05hello"
        anomalies: list[DERAnomaly] = []
        _, length, _, _ = read_tlv(buf, 0, anomalies, "test")
        self.assertEqual(length, 5)
        self.assertTrue(
            any(a.kind == "non_minimal_length" for a in anomalies)
        )

    def test_truncated_buffer_raises(self):
        # Just a tag byte, no length.
        with self.assertRaises(ASN1ParseError):
            read_tlv(b"\x30", 0, [], "test")

    def test_empty_buffer_raises(self):
        with self.assertRaises(ASN1ParseError):
            read_tlv(b"", 0, [], "test")

    def test_offset_past_end_raises(self):
        with self.assertRaises(ASN1ParseError):
            read_tlv(b"\x30\x00", 5, [], "test")

    def test_long_form_octets_overrun_raises(self):
        # 84 = "next 4 octets are length", but we only have 2.
        with self.assertRaises(ASN1ParseError):
            read_tlv(b"\x30\x84\x00\x00", 0, [], "test")


