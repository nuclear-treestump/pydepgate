    def test_unknown_algorithm_oid(self):
        # Construct an SPKI shape where the algorithm OID is bogus.
        # 30 LL                          # outer SEQUENCE
        #   30 LL                        # AlgorithmIdentifier SEQUENCE
        #     06 03 99 99 99             # bogus OID: 3.81.1561 (high arcs)
        #   03 02 00 00                  # empty BIT STRING
        bogus_alg = b"\x30\x05\x06\x03\x99\x99\x99"
        bit_string = b"\x03\x02\x00\x00"
        body = bogus_alg + bit_string
        spki = bytes([0x30, len(body)]) + body
        result = classify(spki)
        self.assertEqual(result.kind, "unknown_public_key")


