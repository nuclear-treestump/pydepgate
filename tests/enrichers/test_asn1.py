class TestClassifyAnomalies(unittest.TestCase):
    """classify() should never raise; every malformed input gets an anomaly."""

    def test_empty_input(self):
        result = classify(b"")
        self.assertEqual(result.kind, "unknown_der")
        self.assertTrue(any(a.kind == "too_short" for a in result.anomalies))

    def test_one_byte_input(self):
        result = classify(b"\x30")
        self.assertEqual(result.kind, "unknown_der")
        self.assertTrue(any(a.kind == "too_short" for a in result.anomalies))

    def test_outer_not_sequence(self):
        # INTEGER (0x02) not SEQUENCE (0x30).
        result = classify(b"\x02\x01\x42")
        self.assertEqual(result.kind, "unknown_der")
        self.assertTrue(
            any(a.kind == "not_sequence" for a in result.anomalies)
        )

    def test_unknown_inner_shape(self):
        # SEQUENCE { INTEGER 1 } - too small to be a cert or SPKI.
        result = classify(b"\x30\x03\x02\x01\x01")
        self.assertEqual(result.kind, "unknown_der")

    def test_truncated_inner(self):
        # SEQUENCE claims 100 bytes but provides only the tag.
        result = classify(b"\x30\x64")
        # Should not raise; should record overrun and fall through.
        self.assertEqual(result.kind, "unknown_der")

    def test_extra_trailing_bytes_recorded(self):
        # Valid empty SEQUENCE with garbage tail.
        result = classify(b"\x30\x00garbage")
        self.assertTrue(
            any(a.kind == "extra_trailing_bytes" for a in result.anomalies)
        )

    def test_classify_never_raises_on_random_garbage(self):
        # Stress check: feed the classifier random byte sequences
        # starting with the SEQUENCE tag to make sure we never raise.
        # subTest gives per-iteration failure context.
        for i in range(20):
            with self.subTest(iteration=i):
                payload = b"\x30" + os.urandom(63)
                result = classify(payload)
                self.assertIsInstance(result, DERClassification)
                self.assertIsInstance(result, FormatContext)


