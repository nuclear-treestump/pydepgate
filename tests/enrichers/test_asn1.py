class TestFormatContextMarker(unittest.TestCase):
    """The marker pattern: DERClassification IS-A FormatContext."""

    def test_classification_is_format_context(self):
        result = classify(CERT_VANILLA_RSA2048)
        self.assertIsInstance(result, FormatContext)
        self.assertIsInstance(result, DERClassification)

    def test_format_context_is_frozen(self):
        # Frozen dataclass should reject attribute assignment with
        # FrozenInstanceError. Tests on FormatContext directly to
        # confirm the marker base class itself is frozen, not just
        # the subclass.
        ctx = FormatContext()
        with self.assertRaises(dataclasses.FrozenInstanceError):
            ctx.some_field = "value"

    def test_classification_subclass_is_frozen(self):
        # Same property inherited by subclasses: assigning to a
        # known field also raises.
        result = classify(CERT_VANILLA_RSA2048)
        with self.assertRaises(dataclasses.FrozenInstanceError):
            result.kind = "other"

    def test_subclass_picklable(self):
        # The dataclass shape is supposed to pickle cleanly so
        # results can be cached or sent across processes.
        result = classify(CERT_VANILLA_RSA2048)
        roundtripped = pickle.loads(pickle.dumps(result))
        self.assertEqual(roundtripped.kind, result.kind)
        self.assertEqual(roundtripped.fields, result.fields)


