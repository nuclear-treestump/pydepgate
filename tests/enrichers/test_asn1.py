class TestReadOID(unittest.TestCase):
    """OID round-trips covering small arcs, large arcs, edge cases."""

    def test_rsa_encryption_oid(self):
        # 1.2.840.113549.1.1.1
        encoded = bytes.fromhex("2a864886f70d010101")
        self.assertEqual(read_oid(encoded, [], "t"), "1.2.840.113549.1.1.1")

    def test_two_arc_minimal(self):
        # 1.2 -> 0x2a
        self.assertEqual(read_oid(b"\x2a", [], "t"), "1.2")

    def test_first_arc_zero(self):
        # 0.9 -> 0x09
        self.assertEqual(read_oid(b"\x09", [], "t"), "0.9")

    def test_first_arc_two(self):
        # 2.5.4.3 (commonName) -> 0x55 0x04 0x03
        self.assertEqual(read_oid(b"\x55\x04\x03", [], "t"), "2.5.4.3")

    def test_large_arc_uses_continuation(self):
        # Arc value 113549 = 0x1bb8d -> encoded as 86 f7 0d
        encoded = bytes.fromhex("2a864886f70d")
        result = read_oid(encoded, [], "t")
        self.assertTrue(result.startswith("1.2.840.113549"))

    def test_empty_input_records_anomaly(self):
        anomalies: list[DERAnomaly] = []
        result = read_oid(b"", anomalies, "t")
        self.assertEqual(result, "")
        self.assertTrue(any(a.kind == "empty_oid" for a in anomalies))

    def test_truncated_oid_records_anomaly(self):
        # 0x80 has continuation bit set but is the last byte.
        anomalies: list[DERAnomaly] = []
        result = read_oid(b"\x2a\x80", anomalies, "t")
        self.assertIn("[?]", result)
        self.assertTrue(any(a.kind == "truncated_oid" for a in anomalies))


